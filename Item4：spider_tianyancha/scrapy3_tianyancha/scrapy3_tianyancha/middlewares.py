class MyProxyMiddleware(ProxyMiddleware):
    ALL_EXCEPTIONS = (defer.TimeoutError, TimeoutError, DNSLookupError,
                      ConnectionRefusedError, ConnectionDone, ConnectError,
                      ConnectionLost, TCPTimedOutError, ResponseFailed,
                      IOError, TunnelError)
    def process_request(self, request, spider):
        """
        处理请求数据，
        为请求添加静态代理
        :param request: 请求数据
        :param spider: 爬虫对象
        :return:
        """
        request.meta['proxy'] = 'http://****'
        request.meta['dont_redirect'] = True  # 禁止重定向
        # spider.logger.info("输入地址 {}".format(request.url))

    def process_response(self, request: Request, response, spider):  # 使用实例
        # print('当前请求ip：', request.meta.get('proxy'))
        # spider.logger.info('输出地址 {}'.format(response.url))
        # response.text

        if response.status == 200:
            if "快捷登录" not in response.text or "密码登录" not in response.text:
                # print(response.text)
                return response
            else:
                logger.debug("页面数据异常,再次尝试请求......")
                request.dont_filter = True
                time.sleep(1)
                return request
        else:
            logger.debug("请求出错,再次尝试请求......")
            request.dont_filter = True
            return request

    def process_exception(self, request, exception, spider):
        # 捕获几乎所有的异常
        if isinstance(exception, self.ALL_EXCEPTIONS):
            # 在日志中打印异常类型
            print('Got exception: %s' % (exception))
            # 随意封装一个response，返回给spider
            # response = HtmlResponse(url='exception')
            return request
        # 打印出未捕获到的异常
        print('not contained exception: %s' % exception)

